/**
 * 供給地点22桁から日程コードを抽出（4〜5文字目）
 * 例: '0600122XXXXXXXXXXXXXXXXX' → '01'
 */
export function extractScheduleCode(supplyPoint22: string | null): string | null {
  if (!supplyPoint22 || supplyPoint22.length < 5) return null;
  const code = supplyPoint22.slice(3, 5);
  return SUPPLY_CALENDAR_2026[code] ? code : null;
}

/**
 * 日程コードから直近の供給開始日を返す（今日以降で最も近い日付）
 */
export function getNearestSupplyDate(code: string): string | null {
  const monthMap = SUPPLY_CALENDAR_2026[code];
  if (!monthMap) return null;

  const today = new Date().toISOString().slice(0, 10);
  const allDates = Object.values(monthMap).sort();
  return allDates.find((d) => d >= today) ?? allDates[allDates.length - 1];
}

export interface UpcomingSupplyDate {
  code: string;
  date: string;
  monthLabel: string;  // 例: "5月"
  primary: boolean;    // true = 今月
}

/**
 * 今月〜N ヶ月先の供給開始日一覧を返す
 * @param code   日程コード（2桁文字列）
 * @param months 取得月数（デフォルト2）
 */
export function getUpcomingSupplyDates(
  code: string,
  months = 2,
): UpcomingSupplyDate[] {
  const monthMap = SUPPLY_CALENDAR_2026[code];
  if (!monthMap) return [];

  const today = new Date();
  const results: UpcomingSupplyDate[] = [];

  for (let m = 0; m < months; m++) {
    const t = new Date(today.getFullYear(), today.getMonth() + m, 1);
    const ym =
      t.getFullYear() +
      "-" +
      String(t.getMonth() + 1).padStart(2, "0");
    const date = monthMap[ym];
    if (date) {
      results.push({
        code,
        date,
        monthLabel: String(t.getMonth() + 1) + "月",
        primary: m === 0,
      });
    }
  }
  return results;
}

/**
 * 供給地点22桁から今月・翌月の供給開始日を返す（一括ヘルパー）
 * 戻り値: null = 無効コード、配列 = 0〜2件
 */
export function getSupplyInfoFromPoint(
  supplyPoint22: string | null,
): UpcomingSupplyDate[] | null {
  const code = extractScheduleCode(supplyPoint22);
  if (!code) return null;
  return getUpcomingSupplyDates(code, 2);
}
